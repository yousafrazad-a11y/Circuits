import os
import torch
import json

FIVEWAY_CIRCUIT_PATH = "/home/exouser/pruning/intersection_experiments/results_5way/5WAY_GLOBAL_CIRCUIT.pt"
PAIRWISE_GLOBAL_CIRCUIT_PATH = "/home/exouser/pruning/intersection_experiments/final_analysis/saved_circuits/GLOBAL_UNIVERSAL_CIRCUIT.pt"
OUTPUT_DIR = "/home/exouser/pruning/intersection_experiments/results_5way"

def compute_similarity(mask1, mask2):
    total_1 = 0
    total_2 = 0
    total_intersect = 0
    total_union = 0
    
    for k in mask1.keys():
        # Handle potential missing keys if architectures don't perfectly align (though they should)
        if k not in mask2:
            m1 = mask1[k]
            m2 = torch.zeros_like(m1)
        else:
            m1 = mask1[k]
            m2 = mask2[k]
            
        intersect = m1 & m2
        union = m1 | m2
        
        total_1 += m1.sum().item()
        total_2 += m2.sum().item()
        total_intersect += intersect.sum().item()
        total_union += union.sum().item()
        
    jaccard = total_intersect / total_union if total_union > 0 else 0
    overlap_1 = total_intersect / total_1 if total_1 > 0 else 0
    overlap_2 = total_intersect / total_2 if total_2 > 0 else 0
    
    return {
        "Size_1": total_1,
        "Size_2": total_2,
        "Intersection": total_intersect,
        "Union": total_union,
        "Jaccard": jaccard,
        "Overlap_1": overlap_1,
        "Overlap_2": overlap_2
    }

def extract_head_indices(circuit):
    active_heads = []
    for k, mask in circuit.items():
        if "layers" in k:
            layer_idx = int(k.split("layers.")[1].split(".")[0])
            indices = torch.nonzero(mask).squeeze()
            if indices.dim() == 0:
                indices = [indices.item()]
            else:
                indices = indices.tolist()
            for idx in indices:
                active_heads.append((layer_idx, idx))
    return sorted(active_heads)

def main():
    print("Loading circuits...")
    fiveway_circuit = torch.load(FIVEWAY_CIRCUIT_PATH, weights_only=True)
    pairwise_global = torch.load(PAIRWISE_GLOBAL_CIRCUIT_PATH, weights_only=True)
    
    print("Computing similarity...")
    sim = compute_similarity(fiveway_circuit, pairwise_global)
    
    fiveway_indices = extract_head_indices(fiveway_circuit)
    pairwise_indices = extract_head_indices(pairwise_global)
    
    md = "# 5-Way Joint Intersection vs Pairwise Global Intersection\n\n"
    md += "This analysis compares the **5-Way Global Circuit** (generated by training all 5 datasets simultaneously and branching into 5 specific circuits) against the **Pairwise Global Circuit** (generated by training 10 specific pairs and finding the global intersection of their intersections).\n\n"
    
    md += "## Circuit Similarity\n\n"
    md += "| Metric | Value |\n"
    md += "|---|---|\n"
    md += f"| **Size (5-Way Circuit)** | {sim['Size_1']} heads |\n"
    md += f"| **Size (Pairwise Global Circuit)** | {sim['Size_2']} heads |\n"
    md += f"| **Intersection Size** | {sim['Intersection']} heads |\n"
    md += f"| **Union Size** | {sim['Union']} heads |\n"
    md += f"| **Jaccard Similarity** | {sim['Jaccard']:.4f} |\n"
    md += f"| **Overlap (5-Way inside Pairwise)** | {sim['Overlap_1']:.4f} |\n"
    md += f"| **Overlap (Pairwise inside 5-Way)** | {sim['Overlap_2']:.4f} |\n\n"
    
    md += "## Analysis\n"
    if sim['Jaccard'] == 1.0:
        md += "The two pipelines generated the **EXACT SAME** mathematical circuit! This is incredibly strong evidence for absolute structural convergence.\n\n"
    else:
        md += "The pipelines generated highly similar, but distinct circuits.\n\n"
        
    md += "### Exact Head Indices: 5-Way Circuit\n"
    md += "```json\n"
    md += json.dumps(fiveway_indices, indent=2) + "\n"
    md += "```\n\n"
    
    md += "### Exact Head Indices: Pairwise Global Circuit\n"
    md += "```json\n"
    md += json.dumps(pairwise_indices, indent=2) + "\n"
    md += "```\n"
    
    out_path = os.path.join(OUTPUT_DIR, "5way_vs_pairwise_analysis.md")
    with open(out_path, "w") as f:
        f.write(md)
        
    print(f"Analysis saved to {out_path}")
    print(f"Jaccard Similarity: {sim['Jaccard']:.4f}")
    print(f"Sizes: 5-way ({sim['Size_1']}), Pairwise ({sim['Size_2']})")

if __name__ == "__main__":
    main()
